async function addToMetamask() {
    const tokenSymbol = 'TCO';
    const tokenDecimals = 18;
    const tokenImage = 'https://petertill.github.io/assets/brand/token.png';

    try {
        // wasAdded is a boolean. Like any RPC method, an error may be thrown.
        const wasAdded = await window.ethereum.request({
            method: 'wallet_watchAsset',
            params: {
            type: 'ERC20', // Initially only supports ERC20, but eventually more!
            options: {
                address: ContractAddress, // The address that the token is at.
                symbol: tokenSymbol, // A ticker symbol or shorthand, up to 5 chars.
                decimals: tokenDecimals, // The number of decimals in the token
                image: tokenImage, // A string url of the token logo
            },
            },
        });
    } catch (error) {
    console.log(error);
    }
}

let Contract = undefined;
let signer = undefined;

const provider = new ethers.providers.Web3Provider(window.ethereum, "maticmum");

provider.send("eth_requestAccounts", []).then(() => {
  provider.listAccounts().then((accounts) => {
    signer = provider.getSigner(accounts[0]);
    Contract = new ethers.Contract(ContractAddress,ABI,signer);
  });
});

async function getHistoricalTokenPriceEvents() {
	const filter = Contract.filters.TokensBought(); // Use the event filter
  	const logs = await Contract.queryFilter(filter);
	return logs.map(log => ({
		buyer: log.args.buyer,
		numberOfTokens: log.args.numberOfTokens.toString(),
		totalPrice: log.args.totalPrice.toString(),
		tokenPrice: log.args.tokenPrice.toString()
	}));
}

//On body load
$(document).ready(async function() {
	console.log("Web3.js loaded");
	const historicalTokenPriceEvents = await getHistoricalTokenPriceEvents();
	console.log(historicalTokenPriceEvents);
});

async function fetchPrice() { //One token is worth this much (0.5)
    let price = await Contract.calculateTokenPrice();
    return ethers.utils.formatEther( price );
    //console.log(price);
}

// Buy
$('#b-tco').on('input', async function(e){
    const token = $("#b-tco").val();
    if (isNaN(token)) {
      alert("Please enter a valid number for Matic.");
      return;
    }
    $('#s-matic').html(token*await fetchPrice());
});

async function buyTokens() {
    const price = await fetchPrice();
    const token = $("#b-tco").val();
    alert("You are about to buy " + token + " TCO for " + (token*price).toString() + " Matic.");
    const valueInWei = ethers.utils.parseEther((token*price).toString());
  
    const transaction = await Contract.buyTokens(parseInt(token), {
      value: valueInWei,
      gasLimit: 9000000,
    });
    await transaction.wait();
}

